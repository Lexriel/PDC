<!-- la class FORMULE pour les tags <p> -->
 
<p>
L'évaluation s'effectue suivant une procédure de contrôle continu, et un examen en fin de semestre. 
</p>


<p>
Trois notes seront attribuées à chaque étudiant durant le semestre :
</p>

<ul>
<li> <span class="NOTE">DS1</span> : une note sur 20 de Travaux Dirigés
(interrogations écrites en amphi, ...), attribuée par l'enseignant de Travaux
Dirigés. </li>
<li> <span class="NOTE">TP</span> : une note de Travaux Pratiques   </li> 
<li> <span class="NOTE">DS2</span> : une note sur 20 pour l'examen de fin de semestre. </li> 
</ul>




<p>
La note finale sur 20 (<span class="NOTE">N</span>) est calculée comme
une moyenne pondérée de ces trois notes : 
</p>


<p class="FORMULE">
   DS = sup ( DS1+2DS2/3,DS2)

<br>
   N= (TP+3 DS)/4
</p>



<p> Pour la seconde session d'examen, la note TD (<span class="NOTE">CC</span>) est
conservée. La note d'examen  (<span class="NOTE">EX</span>) 
est remplacée par la note obtenue lors de la seconde session. 
</p>
 

<p>
L'unité acquise apporte 5 ECTS.
</p>

