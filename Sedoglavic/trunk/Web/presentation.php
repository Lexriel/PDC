<h3>Volume horaire</h3>

<p>Cette unité se déroule au <a href="http://www.fil.univ-lille1.fr/portail/ls5" >S5</a> de la
licence LST A - mention informatique. Il s'agit d'une UE obligatoire
de cette mention.</p>

<p>L'enseignement est organisé sous la forme d'une séance hebdomadaire
d'1h30 de cours, d'une séance hebdomadaire d'1h30 de TD et, pratique
oblige, d'une séance hebdomadaire de 2h de travaux pratiques.</p>

<p>L'UE PDC est un pré-requis indispensable au suivi de l'UE <a
href="http://www.fil.univ-lille1.fr/portail/ls6/pds" >PDS</a>, programmation des systèmes du <a
href="http://www.fil.univ-lille1.fr/portail/ls6" >S6</a>.</p> 

